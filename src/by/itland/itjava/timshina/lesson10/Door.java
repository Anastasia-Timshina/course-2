package by.itland.itjava.timshina.lesson10;

public class Door {
    private int h;
    private int w;

    public Door() {
    }

    public Door(int h, int w) {
        this.h = h;
        this.w = w;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }
}
