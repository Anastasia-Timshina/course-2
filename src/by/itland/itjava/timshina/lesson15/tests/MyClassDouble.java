package by.itland.itjava.timshina.lesson15.tests;

public class MyClassDouble {
    double a;
    double b;

    public MyClassDouble(double a, double b) {
        this.a = a;
        this.b = b;
    }

    @Override
    public String toString() {
        return "myClassDouble{" +
                "a='" + a + '\'' +
                ", b='" + b + '\'' +
                '}';
    }
}
