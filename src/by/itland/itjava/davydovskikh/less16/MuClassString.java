package by.itland.itjava.davydovskikh.less16;

public class MuClassString {
    String a;
    String b;

    public MuClassString(String a, String b) {
        this.a = a;
        this.b = b;
    }

    @Override
    public String toString() {
        return "MuClassString{" +
                "a=" + a +
                ", b=" + b +
                '}';
    }
}
